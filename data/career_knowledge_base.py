def search_careers_by_skill(skill):
    """Find careers that require a specific skill"""
    matching_careers = []
    skill = skill.lower()
    
    for category, careers in CAREER_KNOWLEDGE_BASE.items():
        for career_name, career_info in careers.items():
            skills = [s.lower() for s in career_info.get("skills_required", [])]
            if any(skill in s for s in skills):
                matching_careers.append({
                    "name": career_name.replace("_", " ").title(),
                    "category": category,
                    "description": career_info["description"]
                })
    
    return matching_careers

def get_salary_comparison(careers):
    """Compare salaries across multiple careers"""
    comparison = []
    
    for career in careers:
        career_name = career.lower().replace(" ", "_")
        career_info = get_career_info(career_name)
        if career_info:
            comparison.append({
                "career": career,
                "salary_range": career_info.get("salary_range", "Not available"),
                "job_outlook": career_info.get("job_outlook", "Not available")
            })
    
    return comparison